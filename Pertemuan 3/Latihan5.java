public class Lat5{	
    public static void main(String[] args){
       int a, b;	
		
        a = 5;		
        b = 4;		
        System.out.println("Nilai A	= " + a);
        System.out.println("Nilai ++A = " + ++a);
        System.out.println("Nilai A	= " + a);
        System.out.println("Nilai A++	= " + a++);
        System.out.println("Nilai A	= " + a);
        System.out.println("----------------------	");
        System.out.println("Nilai B	= " + b);
        System.out.println("Nilai ++B = " + ++b);
        System.out.println("Nilai B++	= " + b++);
        System.out.println("Nilai B	= " + b);
    }
}	
