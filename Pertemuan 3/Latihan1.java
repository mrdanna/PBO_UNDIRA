public class Latihan1{			
    public static void main(String[] args){			
        System.out.println("Hasil dari	7	x 4 = " + (7 * 4));
        System.out.println("Hasil dari	7	/ 4 = " + (7 / 4));
        System.out.println("Hasil dari	7	% 4 = " + (7 % 4));
        System.out.println("Hasil dari	7	+ 4 = " + (7 + 4));
        System.out.println("Hasil dari	7	- 4 = " + (7 - 4));
    }			
}