    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Pilih baris data yang akan diedit!");
                return;
            }

            // Ambil ID dari tabel (kolom pertama)
            int id = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());

            String sql = "UPDATE mahasiswa SET nim = ?, nama = ?, jurusan = ? WHERE id = ?";
            Connection conn = Koneksi.getConnection();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtNim.getText());
            pst.setString(2, txtNama.getText());
            pst.setString(3, txtJurusan.getText());
            pst.setInt(4, id);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
            loadData();
            clearForm();
            btnEdit.setEnabled(false);
            btnHapus.setEnabled(false);
            btnSimpan.setEnabled(true); // jika kamu menonaktifkannya saat edit
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal edit: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

