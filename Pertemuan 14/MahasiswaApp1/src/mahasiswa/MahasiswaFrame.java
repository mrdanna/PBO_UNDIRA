    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         if (evt.getClickCount() == 1) { // klik 2 kali
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            // Ambil data dari tabel
            String nim = jTable1.getValueAt(selectedRow, 1).toString(); // Kolom 1: NIM
            String nama = jTable1.getValueAt(selectedRow, 2).toString(); // Kolom 2: Nama
            String jurusan = jTable1.getValueAt(selectedRow, 3).toString(); // Kolom 3: Jurusan

            // Set data ke textfield
            txtNim.setText(nim);
            txtNama.setText(nama);
            txtJurusan.setText(jurusan);

            // Aktifkan tombol edit dan hapus
            btnEdit.setEnabled(true);
            btnHapus.setEnabled(true);
            btnSimpan.setEnabled(false);
        }
    }
    }//GEN-LAST:event_jTable1MouseClicked

